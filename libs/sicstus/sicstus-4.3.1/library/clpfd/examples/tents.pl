/***
The Tents Puzzle.

Place tents in the grid. Each tree should have one tent next to it
(horizontally or vertically. Tents do not touch each other, not even
diagonally. The numbers outside the grid show how many tents in that
row or column.

P. Szeredi, "Teaching Constraints through Logic Puzzles",
Proc. CSCLP 2003, LNAI 3010, pp. 196-222, Springer, 2004.

Instances 20, 31, 32 are hard.
***/

tents(ID) :-
	data(ID, t(ColCounts,RowCounts,Trees)),
	length(RowCounts, NR),
	length(ColCounts, NC),
	(   for(I,1,NR),
	    fromto(KeyZeros,K1,K3,[]),
	    param(NC)
	do  (   for(J,1,NC),
		fromto(K1,[(I-J)-0|K2],K2,K3),
		param(I)
	    do  true
	    )
	),
	(   foreach(X-Y,Trees),
	    fromto(KeyTents,Pairs0,Pairs,[]),
	    foreach(object(K,SID,[X,Y]),Objects),
	    foreach(SID,SIDs),
	    count(K,1,_),
	    param(NR,NC)
	do  tent_cands(X, Y, SID, Vals, NR, NC, Pairs0, Pairs),
	    list_to_fdset(Vals, Set),
	    SID in_set Set
	),
	geost(Objects, [sbox(1,[-1,0],[2,2]),
			sbox(2,[0,-1],[2,2]),
			sbox(3,[0,1] ,[2,2]),
			sbox(4,[1,0] ,[2,2])]),
	append(KeyZeros, KeyTents, L1),
	keysort(L1, L2),
	keyclumped(L2, L3),
	length(Board, NC),
	(   foreach(Col,Board),
	    param(RowCounts)
	do  same_length(RowCounts,Col)
	),
	append(Board, Cells),
	(   foreach(C,Cells),
	    foreach(_-[0|Terms],L3)
	do  (   Terms==[] -> C = 0
	    ;   Terms = [D] -> C = D
	    ;   C in 0..1, sum(Terms, #=, C)
	    )
	),
	(   foreach(Col1,Board),
	    foreach(Count1,ColCounts)
	do  rowsum(Col1, Count1)
	),
	transpose(Board, BoardT),
	(   foreach(Row2,BoardT),
	    foreach(Count2,RowCounts)
	do  rowsum(Row2, Count2)
	),
	labeling([ff,enum], SIDs),
	format('+~*c+\n', [NC,0'-]),
	(   foreach(Row3,BoardT),
	    count(Y1,1,_),
	    param(Trees)
	do  put_code(0'|),
	    (   foreach(Cell,Row3),
		count(X1,1,_),
		param(Trees,Y1)
	    do  (   Cell=:=1 -> Char = 0'T
		;   memberchk(X1-Y1, Trees) -> Char = 0'Y
		;   Char = 0' 
		),
		put_code(Char)
	    ),
	    put_code(0'|),
	    nl
	),
	format('+~*c+\n', [NC,0'-]).

tent_cands(X, Y, SID, Vals, NR, NC) -->
	{Xm is X-1},
	{Xp is X+1},
	{Ym is Y-1},
	{Yp is Y+1},
	(   {Ym<1} -> {Vals=Vals1}
	;   {Vals = [2|Vals1]},	% south
	    % {SID #= 2 #<=> B1},
	    [(X-Ym)-B1]
	),
	(   {Xm<1} -> {Vals1=Vals2}
	;   {Vals1 = [1|Vals2]}, % west
	    % {SID #= 1 #<=> B2},
	    [(Xm-Y)-B2]
	),
	(   {Yp>NR} -> {Vals2=Vals3}
	;   {Vals2 = [3|Vals3]}, % north
	    % {SID #= 3 #<=> B3},
	    [(X-Yp)-B3]
	),
	(   {Xp>NC} -> {Vals3=[]}
	;   {Vals3 = [4]},	% east
	    % {SID #= 4 #<=> B4},
	    [(Xp-Y)-B4]
	),
	{bool_channel([B2,B1,B3,B4], SID, #=, 1)}. % 4.3

rowsum(Row, -1) :- !,
	automaton(Row, [source(0),sink(0),sink(1)], [arc(0,0,0),arc(0,1,1),arc(1,0,0)]).
rowsum(Row, Sum) :- Sum<2, !,
	sum(Row, #=, Sum).
rowsum(Row, Sum) :-
	(   fromto(Sum,S,R,1),
	    fromto(0,I,K,Pre),
	    fromto(Arcs,[arc(I,0,I),arc(I,1,J),arc(J,0,K)|Tail],Tail,Rest)
	do  J is I+1,
	    K is J+1,
	    R is S-1
	),
	Sink is Pre+1,
	Rest = [arc(Pre,0,Pre),arc(Pre,1,Sink),arc(Sink,0,Sink)],
	automaton(Row, [source(0),sink(Sink)], Arcs).

